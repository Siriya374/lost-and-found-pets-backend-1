class places {
  constructor(
    id,
    name,
    latitude,
    longitude,
    created_at,
    updated_at,
  ) {
    this.id = id;
    this.name = name;
    this.latitude = latitude;
    this.longitude = longitude;
    this.created_at = created_at;
    this.updated_at = updated_at;
  }
}

export default places;
