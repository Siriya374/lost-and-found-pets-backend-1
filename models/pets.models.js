class pets {
  constructor(
    id,
    name,
    species,
    breed,
    gender,
    detail,
    user_id,
    created_at,
    updated_at,
  ) {
    this.id = id;
    this.name = name;
    this.species = species;
    this.breed = breed;
    this.gender = gender;
    this.detail = detail;
    this.user_id = user_id;
    this.created_at = created_at;
    this.updated_at = updated_at;

  }
}

export default pets;
